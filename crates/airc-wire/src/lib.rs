//! `airc-wire` ŌĆö the single well-defined binary codec for
//! [`airc_bus::Envelope`], airc's wire + IPC data plane.
//!
//! # Why a binary FlatBuffer
//!
//! airc is a media-stream bus (WebRTC video/audio, 60-90Hz avatar pose
//! data) *and* a durable chat bus. The encoding has to be a proper binary
//! encoding ŌĆö not JSON / escapes ŌĆö and the opaque [`Envelope::payload`]
//! must be forwarded **verbatim, zero-copy**: never decoded, re-encoded,
//! or copied. The render loop is sacred.
//!
//! This crate is that encoding. It is built on [`planus`] ŌĆö a pure-Rust
//! FlatBuffers compiler and runtime that produces spec-compliant
//! FlatBuffers bytes with **no C++ `flatc`** at build time, so CI stays
//! green on ubuntu/macos/windows with the stock Rust toolchain.
//!
//! ## Source of truth & codegen
//!
//! The `.fbs` schema in `src/schema/wire.fbs` is the source of truth. The
//! Rust accessors in `src/wire_generated.rs` are produced from it by the
//! `planus` CLI and **committed** (deterministic, no `build.rs`):
//!
//! ```text
//! planus rust -o crates/airc-wire/src/wire_generated.rs \
//!     crates/airc-wire/src/schema/wire.fbs
//! ```
//!
//! # API
//!
//! - [`encode`] builds the FlatBuffer once ŌĆö the one sanctioned encode.
//! - [`WireEnvelope`] is a zero-copy read view over `&[u8]`: it exposes the
//!   routing fields (kind, delivery, channel, seq, correlation_id, headers)
//!   and crucially [`WireEnvelope::payload`], a slice INTO the buffer (no
//!   copy). Routing reads fields in place without materializing the whole
//!   [`Envelope`].
//! - [`decode`] reconstructs the Rust [`Envelope`]. Small metadata fields
//!   are read out (cheap); the big `payload` is reconstructed with
//!   [`bytes::Bytes::slice_ref`], which shares the decoded buffer's
//!   allocation ŌĆö **never a copy**.

